let a = 12; //assigns value 12 to variable a

a += 5; //equivalent to a = a + 5, now a is 17
a -= 3; //equivalent to a = a - 3, now a is 14
a *= 2; //equivalent to a = a * 2, now a is 28
a /= 4; //equivalent to a = a / 4, now a is 7
a %= 3; //equivalent to a = a % 3, now a is 1
a **= 2; //equivalent to a = a ** 2, now a is 1 (1 raised to the power of 2 is still 1)
console.log(a); // Output: 1