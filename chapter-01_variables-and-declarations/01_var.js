// Variables
// Creating a variable using "var"
var name = "Tanya";
console.log(name); // Output: Tanya


// Creating a variable using "let"
let city = "Delhi";
console.log(city); // Output: Delhi


// Creating a variable using "const"
const pi = 3.14;
console.log(pi); // Output: 3.14 and cannot be reassigned

